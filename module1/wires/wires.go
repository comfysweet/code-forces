func getSlice() (n int, k int, res []int) {
	val := make([]string, 0)
	res = make([]int, 0)
	var v int
	scan := bufio.NewScanner(os.Stdin)
	scan.Scan()
	text := scan.Text()
	val = strings.Split(text, " ")
	n, _ = strconv.Atoi(val[0])
	k, _ = strconv.Atoi(val[1])
	for i := 0; i < n; i++ {
		fmt.Scanln(&v)
		res = append(res, v)
	}
	return
}

func getMax(in []int) (m int) {
	for i, e := range in {
		if i == 0 || e < m {
			m = e
		}
	}
	return
}
