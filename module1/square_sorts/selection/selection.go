func main() {
	n, v := utils.GetSlices()

	result := selectionSort(n, v)
	fmt.Print(result)
}

func selectionSort(n int, v []int) []int {
	for i := 0; i < n-1; i++ {
		least := i
		for j := i + 1; j < n; j++ {
			if v[j] < v[least] {
				least = j
			}
		}
		v[i], v[least] = v[least], v[i]
	}
	return v
}
