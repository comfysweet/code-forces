func main() {
	n, v := utils.GetSlices()

	result := insertionSort(n, v)
	fmt.Print(result)
}

func insertionSort(n int, v []int) []int {
	for j := 1; j < n; j++ {
		key := v[j]
		i := j - 1
		for i >= 0 && v[i] > key {
			v[i+1] = v[i]
			i = i - 1
		}
		v[i+1] = key
	}
	return v
}
