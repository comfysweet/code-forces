func main() {
	n, v := utils.GetSlices()

	result := bubbleSort(n, v)
	fmt.Print(result)
}

func bubbleSort(n int, v []int) []int {
	for j := 0; j < n-1; j++ {
		for i := 0; i < n-1-j; i++ {
			if v[i] > v[i+1] {
				v[i], v[i+1] = v[i+1], v[i]
			}
		}
	}
	return v
}
