func getSlice() []int {
	v := make([]int, 0)
	scanner := bufio.NewScanner(os.Stdin)
	scanner.Scan()
	a := scanner.Text()
	if len(a) == 0 {
		panic("string is empty")
	}
	val := strings.Split(a, " ")
	for _, s := range val {
		i, err := strconv.Atoi(s)
		if err != nil {
			panic("error when cast string to int")
		}
		v = append(v, i)
	}
	return v
}
